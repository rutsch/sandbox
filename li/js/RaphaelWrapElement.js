(function() {
	var initialized = false;


	function init(paper) {
		var root = paper.canvas;

		var g = document.createElementNS('http://www.w3.org/2000/svg', 'g');
		g.id = 'viewport';
		root.appendChild(g);
		paper.canvas = g;




	}

	Raphael.fn.ZPD = function() {
		if (!initialized) init(this);
		return this;
	}
})();
